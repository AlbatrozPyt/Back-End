namespace POO_Calculadora
{
    public class Calculadora
    {
        public float num1;
        public float num2;

        // Metodos
        
        // SOMA
        public float Somar() {
            return num1 + num2;
        }

        public float Subtrair() {
            return num1 - num2;
        }

        public float Multiplicar() {
            return num1 * num2;
        }

        public float Dividir() {
            return num1 / num2;
        }
    }
}